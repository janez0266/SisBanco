<h1>Bibliografía Nueva y Edición:</h1>
La nueva bibliografía y página de edición de campos (Edición básica) están basados en los formatos estándares
<a href="javascript:popSecondaryLarge('http://www.loc.gov/marc')">MARC 21</a>.
Sólo un pequeño subconjunto de los varios cientos de campos MARC es incluido sobre esta
página de edición básica para que sea más fácil agregar bibliografías. 
Una vez que una bibliografía ha sido agregada, podrá completar mas campos MARC 21 usando la página Edit-MARC.
<br><br>
Una vez que usted ha añadida una nueva bibliografía, tendrá que añadir las copias o ejemplares correspondientes a esta
antes de que esté disponible para la comprobación o Consulta.
